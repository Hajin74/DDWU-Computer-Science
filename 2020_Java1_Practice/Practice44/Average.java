public class Average {
	public int getAverage(int a, int b) {
		return (a + b) / 2;
	}
	public int getAverage(int a, int b, int c) {
		return (a + b + c) / 3;
	}
	public double getAverage(double a, double b) {
		return (a + b) / 2.0;
	}
	public double getAverage(double a, double b, double c) {
		return (a + b + c) / 3.0;
	}
}
